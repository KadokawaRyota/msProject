using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.UI;

//------------------------------------------------------------------------------
//          定義
//------------------------------------------------------------------------------
public enum TIPS_LIST
{
	NONE = 0,      
	TEST00,    
	TEST01,  
	MAX,
};

//------------------------------------------------------------------------------
//          メイン
//------------------------------------------------------------------------------
public class TalkTipsManager : MonoBehaviour 
{
	//--------------------------------------------------------------------------
	//          パブリックオブジェクト
	//--------------------------------------------------------------------------
	public GameObject[] Tips;

	//--------------------------------------------------------------------------
	//          変数定義
	//--------------------------------------------------------------------------
	public static TIPS_LIST tips;
    public static bool bEnableFlug;

    //--------------------------------------------------------------------------
    //          初期化処理
    //--------------------------------------------------------------------------
	void Start () 
    {
        //tips = TIPS_LIST.NONE;	
        //bEnableFlug = false;

		tips = TIPS_LIST.NONE;
		Tips [(int)tips].SetActive (true);
		bEnableFlug = true;
	}
	
	//--------------------------------------------------------------------------
	//          更新処理
	//--------------------------------------------------------------------------
	void Update () 
    {
		if (bEnableFlug) 
		{
			Tips [(int)tips].SetActive (true);
			bEnableFlug = false;
		}
	}

	//--------------------------------------------------------------------------
	//          表示Tips情報セット関数
	//--------------------------------------------------------------------------
    public static void SetTips(TIPS_LIST newtips)
    {
        tips = newtips;
        bEnableFlug = true;
    }

	//--------------------------------------------------------------------------
	//          現在のTipsList番号取得
	//--------------------------------------------------------------------------
	public static TIPS_LIST GetTipsList()
	{
		return tips;
	}
}